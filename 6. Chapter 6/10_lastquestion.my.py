'''Write a program to calculate the grade of the student from his marks from the 
following scheme'''

marks=int(input("enter your marks"))

if(marks>=90):
    print("ex")
elif(marks>=80):
    print("a")
elif(marks>=70):
    print("b")
elif(marks>=60):
    print("c")
elif(marks>=50):
    print("d")
elif(marks<50):
    print("fail")
